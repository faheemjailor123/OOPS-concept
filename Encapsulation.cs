using System;
  
public class Encap {
 
    private String studentName;
    private int studentAge; 
  
    public String Name
    {
       get
        {
            return studentName;    
        }
          
        set 
        {
            studentName = value;
        }
          
    }
     
    public int Age
    {
          
        get 
        {
            return studentAge;    
        }
          
        set 
        {
            studentAge = value;
        }
          
    }
  
      
}
  
